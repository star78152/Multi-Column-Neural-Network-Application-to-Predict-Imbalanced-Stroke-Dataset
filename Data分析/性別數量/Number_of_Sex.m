load('testdata');

x = test_input;
figname = 'Test-Number of Sex';
data_size = size(x(:,1))
z = [0;0;0];

for i = 1:data_size
    if x(i,1) ==1
        z(1,1)= z(1,1)+1;
        
    elseif x(i,1) ==2
        z(2,1)=z(2,1)+1;
    elseif x(i,1) ==3
        z(3,1)=z(3,1)+1;
    end
end

b = bar(z);
title(figname);
xlabel('分類類別');			% x 軸的說明文字
ylabel('數量');	% y 軸的說明文字
% 下列指令將 x 軸的數目字改成月數
set(gca, 'xticklabel', {'男','女','其他'});

%顯示長條圖的值
xtips1 = b(1).XEndPoints;
ytips1 = b(1).YEndPoints;
labels1 = string(b(1).YData);
text(xtips1,ytips1,labels1,'HorizontalAlignment','center',...
    'VerticalAlignment','bottom')

saveas(gcf,figname, 'jpg')

